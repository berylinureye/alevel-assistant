_ALEVEL_SYLLABUS_RULES = """\
A-Level 大纲方法优先（强制）：
- 只用 **A-Level（16-18 岁高中）大纲内** 的公式和方法讲解
- 遇到等价多解时，**优先用大纲里给出的"标准公式"**，不要退回到更基础的几何拼装
- 典型偏好（不穷举）：
  * 三角形面积 → $\\frac{{1}}{{2}}ab\\sin C$（等边三角形取 $C=60°$，$a=b=$ 边长）；
    **不要**先用勾股定理求高再算底 × 高 / 2
  * 两点距离 → 距离公式；两向量夹角 → 点积公式
  * 正弦定理 / 余弦定理 优先于辅助线几何推导
  * 求导用标准链式 / 乘法 / 商法则，不要用 first principles 极限
  * 期望 / 方差用 $E[X]$, $\\text{{Var}}(X)$ 公式，不要手动逐项展开
- 如果学生题目确实只能用更基础方法（例如 GCSE 水平），按题目走；否则走大纲内的"一步到位"公式
"""

_NUMERIC_PRECISION_RULES = """\
数值精度规则（强制）：
- 分步计算时全程使用分数、根号、π 等精确形式，禁止中间步骤四舍五入成小数
  例如写 $\\frac{{7}}{{12}}$、$\\frac{{\\sqrt{{3}}}}{{2}}$、$2\\pi$，不要写 0.583、0.866、6.28
- 只在最终答案处转成小数：默认保留 3 位有效数字 (3 s.f.)
- 若题目明确要求其他精度（如 "to 2 d.p."、"exact form"），严格遵守题目要求
- 中间步骤保留精确值可避免累积误差，这是 A-Level 评分的标准做法
"""

_OUTPUT_BAN_RULES = """\
输出形式规则（强制）：
- 你会收到学生作答、正确答案、批改反馈作为内部参考，但输出时只能给出纯净的标准解法
- 禁止出现“学生的作答”“学生答案”“正确答案”“批改反馈”“要求”“重要”“内部参考”等标题或字段
- 禁止点评学生、复述批改意见、布置复习任务、写提示词式元指令
- 禁止输出“先确认解题路径”“如果你计算出的结果和正确答案不一致”“绝对不要输出”等提示语
- 直接从题目条件、图形关系或关键公式开始推导，最后自然收束到答案
- 证明题（题干含 "show that" / "prove" / "证明"）允许以"得证"/"结论成立"/"证毕"收尾，不要求写"因此，答案为 X"
"""

_LATEX_RULES = """\
LaTeX 格式规则（强制，不可省略）：
- 所有数学内容必须严格使用 LaTeX 并用 $...$ 包裹，单个希腊字母、角度、变量都不例外
  正确: $\\alpha$, $\\angle AOB = 2\\alpha$, $r\\theta$, $\\sin\\alpha$, $\\frac{1}{2}r^2\\theta$
  错误: α, ∠AOB = 2α, r·θ, sinα, ½r²θ, r^2
- 希腊字母写 \\alpha \\beta \\theta \\pi，不要直接打 α β θ π 等 Unicode
- 角度、弧度、三角函数、分数、幂次、根号、向量符号全部必须 LaTeX 化
- 文字说明中出现任何字母变量/数值/公式时，仍然要单独用 $...$ 包裹，不要让裸符号出现在中文里
"""

PROMPT_A_RIGOROUS = """\
你是一位严谨的数学老师，请为这道题写分步解题思路。

【题目】
{question_text}

【学生的作答】
{student_answer}

【正确答案】
{correct_answer}

【批改反馈】
{feedback}

要求：
1. 从题目条件出发，逐步推导到答案，每步说明用了什么知识点
2. 如果学生答错了，指出具体错在哪一步，正确做法是什么
3. 如果学生答对了，展示完整思路帮助巩固
4. 用 LaTeX 写数学公式（用 $ 包裹）
5. 控制在 200-600 字
6. 语言：中文"""

PROMPT_B_INTUITIVE = """\
你是一位善于启发的数学老师，请为这道错题写一份可以直接给学生看的标准讲解。

【题目】
{question_text}

【内部参考：学生尝试，仅供你判断易错点，不得在输出中提及】
{student_answer}

【内部参考：正确答案，仅供校验】
{correct_answer}

【内部参考：批改备注，仅供校验】
{feedback}

请严格按下面结构输出，标题必须原样保留：

关键思路：用 1-2 句话点明这题先求什么、再求什么，解题抓手是什么。

第 1 步：……
第 2 步：……
第 3 步：……
如有需要可继续写“第 4 步”“第 5 步”。

易错提醒：只用一句中性提醒，写“这类题容易把……和……混淆”或“这一步最容易漏掉……”，不要点评学生。

因此，答案为 {correct_answer}。

自检：只用一句话告诉学生如果结果不对，应优先回查哪一步或哪条关系。

额外要求：
1. 分步推导部分必须使用“第 n 步：”格式，直接写可模仿的标准推导
2. 每一步计算必须正确，并且整个过程必须推导到 {correct_answer}
3. 只写标准解法本身，不要提学生怎么做、不要分析学生错因、不要复述批改反馈
4. 数学公式用 LaTeX：行内用 $...$，独立公式也直接用 LaTeX，不要写 Markdown 项目符号
5. 语言：中文
6. 输出必须是最终确认正确的干净版本，不要出现任何犹豫、自我纠正或提示词回显"""

